    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed

       String username = usernameTextField.getText();
       String password = String.valueOf(passwordField.getPassword());
               for(Organization organization : business.getOrganizationDirectory().getOrganizationList()){
                  UserAccount userAccount = organization.getUserAccountDirectory().authenticateUser(username,password);
                  if (null != userAccount){
                      JPanel adminWorkAreaJPanel = userAccount.getRole().createWorkArea(userProcessContainer, userAccount, organization, business);
                      userProcessContainer.add("AdminWorkAreaJPanel",adminWorkAreaJPanel);
                      ((CardLayout)userProcessContainer.getLayout()).next(userProcessContainer);                      
                      break;
                  }                 
        } 
    }//GEN-LAST:event_loginButtonActionPerformed

