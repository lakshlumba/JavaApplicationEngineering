    private void refreshTable() {
        DefaultTableModel model = (DefaultTableModel)supplierTable.getModel();
        model.setRowCount(0);
        for(Supplier supplier : supplierDirectory.getSupplierList()) {
            Object row[] = new Object[1];
            row[0] = supplier;
            model.addRow(row);
        }
    }
  

