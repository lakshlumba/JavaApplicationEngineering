    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (validateFields()){
            personInfo.setFirstName(firstNameTextField.getText());
            personInfo.setLastName(lastNameTextField.getText());
            personInfo.setDateOfBirth(birthDateTextField.getText());
            personInfo.setSsn(ssnTextField.getText());
            AddressInfo addressInfo = new AddressInfo();
            addressInfo.setStreetName(streetNameTextField.getText());
            addressInfo.setTown(cityTextField.getText());
            addressInfo.setState(stateTextField.getText());
            addressInfo.setCountry(countryTextField.getText());
            addressInfo.setZipCode(zipCodeTextField.getText());
            personInfo.setAddressInfo(addressInfo);
            clearAll();
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    /**
     * validate the fields before submitting
     */
    private boolean validateFields()
    {
        StringBuffer validationMessage = new StringBuffer();
        boolean isValidationPassed = true;
        if(ValidationUtils.isStringEmptyOrNull(firstNameTextField.getText())){
            validationMessage.append(Consts.FIRST_NAME_VALIDATE_MESSAGE);
            validationMessage.append(Consts.NEXT_LINE);
            isValidationPassed = false;
        }
        if(ValidationUtils.isStringEmptyOrNull(lastNameTextField.getText())){
            validationMessage.append(Consts.LAST_NAME_VALIDATE_MESSAGE);
            validationMessage.append(Consts.NEXT_LINE);
            isValidationPassed = false;
        }
        if(ValidationUtils.isStringEmptyOrNull(birthDateTextField.getText())){
            validationMessage.append(Consts.DOB_NAME_VALIDATE_MESSAGE);
            validationMessage.append(Consts.NEXT_LINE);
            isValidationPassed = false;
        }
        if(ValidationUtils.isStringEmptyOrNull(ssnTextField.getText())){
            validationMessage.append(Consts.SSN_NAME_VALIDATE_MESSAGE);
            validationMessage.append(Consts.NEXT_LINE);
            isValidationPassed = false;
        }
        if(ValidationUtils.isStringEmptyOrNull(streetNameTextField.getText())){
            validationMessage.append(Consts.STREET_NUMBER_VALIDATE_MESSAGE);
            validationMessage.append(Consts.NEXT_LINE);
            isValidationPassed = false;
        }
        if(ValidationUtils.isStringEmptyOrNull(cityTextField.getText())){
            validationMessage.append(Consts.CITY_VALIDATE_MESSAGE);
            validationMessage.append(Consts.NEXT_LINE);
            isValidationPassed = false;
        }
        if(ValidationUtils.isStringEmptyOrNull(stateTextField.getText())){
            validationMessage.append(Consts.STATE_VALIDATE_MESSAGE);
            validationMessage.append(Consts.NEXT_LINE);
            isValidationPassed = false;
        }
        if(ValidationUtils.isStringEmptyOrNull(countryTextField.getText())){
            validationMessage.append(Consts.COUNTRY_VALIDATE_MESSAGE);
            validationMessage.append(Consts.NEXT_LINE);
            isValidationPassed = false;
        }
        if(ValidationUtils.isStringEmptyOrNull(zipCodeTextField.getText())){
            validationMessage.append(Consts.ZIPCODE_VALIDATE_MESSAGE);
            validationMessage.append(Consts.NEXT_LINE);
            isValidationPassed = false;
        }     
        if(!isValidationPassed){
            (new JOptionPane()).showMessageDialog(null,validationMessage);
        } 
        return isValidationPassed;
    }
    /**
     * clear all the test fields after submit and clear button 
     */
